    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double Valor_Conta;
        if(!"".equals(txtTipo_Tel.getText()) && !"".equals(txtQtde_Pulso.getText()) && !"".equals(txtQtde_Inter.getText()) && !"".equals(txtQtde_Desp.getText())){
            if(Integer.parseInt(txtTipo_Tel.getText()) == 1){
                Valor_Conta = 10;
            } else {
                Valor_Conta = 15;
            }

            if(Integer.parseInt(txtQtde_Pulso.getText()) > 90){
                Valor_Conta = Valor_Conta + (Integer.parseInt(txtQtde_Pulso.getText()) - 90) * 0.05;
            }

            Valor_Conta = Valor_Conta + (Integer.parseInt(txtQtde_Inter.getText()) * 60) * 0.08;
            Valor_Conta = Valor_Conta + (Integer.parseInt(txtQtde_Desp.getText()) * 2);

            lblValor_Conta.setText(Formato.format(Valor_Conta));

            btnCalcular.setEnabled(false);
            btnLimpar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos Tipo, Qtde. Pulso, Qtde. Interurbano e Qtde. Despertador!");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

